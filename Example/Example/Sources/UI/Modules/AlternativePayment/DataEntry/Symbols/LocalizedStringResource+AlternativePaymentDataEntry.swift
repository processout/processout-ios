import Foundation

extension LocalizedStringResource {

    enum AlternativePaymentDataEntry {

        /// Message.
        static let message = LocalizedStringResource("alternative-payment-data-entry.message")

        /// Key.
        static let key = LocalizedStringResource("alternative-payment-data-entry.key")

        /// Value.
        static let value = LocalizedStringResource("alternative-payment-data-entry.value")

        /// Confirm.
        static let confirm = LocalizedStringResource("alternative-payment-data-entry.confirm")
    }
}
